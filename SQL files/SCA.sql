create table courses
	(
		c_code varchar (25),
		c_name varchar (50),
		c_level int,
		c_descrip varchar(500),
		semester int,
		creditnum int,


		primary key (c_code)

	);

create table prereq
(
	id int not null AUTO_INCREMENT,
	c_code varchar(8),
	prereq_code varchar(8),
	primary key (id),
	FOREIGN KEY (c_code) REFERENCES courses(c_code),
	FOREIGN KEY (prereq_code) REFERENCES courses(c_code)

);


